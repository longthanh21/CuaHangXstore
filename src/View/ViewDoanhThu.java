    void loadHD(){
        defau = (DefaultTableModel) tblHoaDon.getModel();
        defau.setRowCount(0);
        int stt = 1;
        for (HoaDon hd : ql.getListHD()) {
            defau.addRow(new Object[]{
                stt++,hd.getMaHD(),hd.getNgayTao(),hd.getTongTien()
            });
        }
    }
